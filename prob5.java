//Q. Sum of natural numbers in given range 

public class prob5{
    public static void main(String[] args){
       int a = 1;
       int b = 3;
       int sum =0;
       for(int i=a;i<=b;i++){
        sum = sum+i;

       }
       System.out.println(sum);
    }
}