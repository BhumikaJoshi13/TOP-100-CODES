//WAP FOR FACTORIAL OF A NUMBER

//Ex. 4! = 4*3*2*1 =24

public class prob13 {
 
    public static void main(String[] args) {
        int num = 10;
        int fact = 1;
        for(int i =1; i<=num;i++){
            fact = fact*i;
        }
        System.out.println(fact);
    }
    
}